function subnet {
  docker network inspect $(docker network create $1) | grep -oP "\"Subnet\".+" | grep -oP "(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\/\d+"
}

{
    docker network create -d bridge npm | subnet
} || {
    subnet npm
}